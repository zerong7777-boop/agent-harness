function normalizeHeroFallbacks() {
  const taskTitle = document.getElementById("taskTitle");
  const taskSummary = document.getElementById("taskSummary");
  const nextHint = document.getElementById("taskNextStageHint");
  if (taskTitle && (!taskTitle.textContent.trim() || /\?/.test(taskTitle.textContent))) {
    v4SetText(taskTitle, V4_TEXT.heroEmptyTitle);
  }
  if (taskSummary && (!taskSummary.textContent.trim() || /\?/.test(taskSummary.textContent))) {
    v4SetText(taskSummary, V4_TEXT.heroEmptySummary);
  }
  if (nextHint && (!nextHint.textContent.trim() || /\?/.test(nextHint.textContent))) {
    v4SetText(nextHint, V4_TEXT.heroNextStep);
  }
}

