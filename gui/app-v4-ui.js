function reorderTabs() {
  const bar = document.querySelector(".task-tab-bar");
  const panels = document.querySelector(".task-tab-panels");
  if (!bar || !panels) return;

  const order = ["plan", "runtime", "overview", "experiment", "session"];
  order.forEach((id) => {
    const button = bar.querySelector(`[data-tab-id="${id}"]`);
    const panel = panels.querySelector(`[data-tab-panel="${id}"]`);
    if (button) {
      v4SetText(button, V4_TEXT.tabTitles[id] || button.textContent);
      bar.append(button);
    }
    if (panel) {
      panels.append(panel);
    }
  });

  const head = document.querySelector(".task-tabs-head h2");
  v4SetText(head, "\u4efb\u52a1\u5de5\u4f5c\u533a");
}

function ensureActiveTab() {
  const bar = document.querySelector(".task-tab-bar");
  const panels = document.querySelector(".task-tab-panels");
  if (!bar || !panels) return;

  const activeButton = bar.querySelector(".task-tab-button.is-active");
  const activePanel = panels.querySelector(".task-tab-panel.is-active");
  if (activeButton && activePanel) {
    return;
  }

  const fallbackButton =
    bar.querySelector('[data-tab-id="plan"]') ||
    bar.querySelector(".task-tab-button");
  const fallbackPanel =
    panels.querySelector('[data-tab-panel="plan"]') ||
    panels.querySelector(".task-tab-panel");
  if (fallbackButton instanceof HTMLButtonElement && fallbackPanel instanceof HTMLElement) {
    bar.querySelectorAll(".task-tab-button").forEach((button) => {
      const isActive = button === fallbackButton;
      button.classList.toggle("is-active", isActive);
      button.setAttribute("aria-selected", isActive ? "true" : "false");
    });
    panels.querySelectorAll(".task-tab-panel").forEach((panel) => {
      panel.classList.toggle("is-active", panel === fallbackPanel);
    });
  }
}

function simplifyWorkspace() {
  const tabsHead = document.querySelector(".task-tabs-head");
  const eyebrow = tabsHead?.querySelector(".eyebrow");
  if (eyebrow) {
    eyebrow.remove();
  }

  const insights = document.querySelector(".insights-grid");
  if (insights) {
    insights.hidden = true;
  }

  const processSection = document.getElementById("processPanel")?.closest("section");
  const processHeading = processSection?.querySelector(".section-head h2");
  v4SetText(processHeading, "\u4f1a\u8bdd\u8fc7\u7a0b");
}

