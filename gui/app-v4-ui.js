function simplifyRuntimePanel() {
  const runtimePanel = document.getElementById("runtimePanel");
  if (!runtimePanel) return;

  const cards = Array.from(runtimePanel.querySelectorAll(":scope > .stack-card"));
  const replyCard = cards.find((card) => card.querySelector(".chat-bubble-primary, .runtime-reply-text, .runtime-reply-empty"));
  const stateCard = cards.find((card) => card.querySelector(".meta-card"));
  const historyCard = cards.find((card) => {
    const heading = card.querySelector("h3");
    return heading && /\u8bb0\u5f55|\u5386\u53f2/.test(heading.textContent);
  });
  const appServerCard = cards.find((card) => card !== replyCard && card !== stateCard && card !== historyCard);

  if (replyCard) {
    const heading = replyCard.querySelector("h3");
    v4SetText(heading, V4_TEXT.runtimeHeadings[0]);
    runtimePanel.prepend(replyCard);
    const recentHead = replyCard.querySelector(".runtime-chat-history-head");
    v4SetText(recentHead, V4_TEXT.runtimeRecentMessages);
    const emptyReply = replyCard.querySelector(".runtime-reply-empty");
    v4SetText(emptyReply, V4_TEXT.runtimeEmptyReply);
  }

  if (stateCard) {
    const heading = stateCard.querySelector("h3");
    v4SetText(heading, V4_TEXT.runtimeHeadings[1]);
  }

  if (historyCard) {
    const heading = historyCard.querySelector("h3");
    v4SetText(heading, V4_TEXT.runtimeHeadings[2]);
  }

  if (appServerCard && !appServerCard.querySelector(".runtime-details")) {
    const heading = appServerCard.querySelector("h3");
    const details = document.createElement("details");
    details.className = "runtime-details";
    const summary = document.createElement("summary");
    summary.textContent = V4_TEXT.runtimeDetailsSummary;
    const body = document.createElement("div");
    body.className = "runtime-details-body";

    Array.from(appServerCard.children).forEach((child) => {
      if (child !== heading) {
        body.append(child);
      }
    });
    details.append(summary, body);
    appServerCard.innerHTML = "";
    appServerCard.append(heading, details);
    v4SetText(heading, V4_TEXT.runtimeHeadings[3]);
  }

  const hint = document.getElementById("runtimeHint");
  const sendButton = document.getElementById("runtimeSendMessageButton");
  v4SetText(hint, sendButton?.disabled ? V4_TEXT.runtimeHintDisconnected : V4_TEXT.runtimeHintConnected);
}

function normalizeGuide() {
  const title = document.querySelector(".guide-drawer-head h2");
  v4SetText(title, V4_TEXT.guideTitle);
  v4SetText(document.getElementById("guideCloseButton"), V4_TEXT.guideClose);

  const guideContent = document.getElementById("guideContent");
  if (!guideContent) return;
  v4SetHTML(
    guideContent,
    V4_TEXT.guideSections
      .map(
        ([heading, body]) => `
          <section class="guide-section">
            <h3>${heading}</h3>
            <p>${body}</p>
          </section>
        `,
      )
      .join(""),
  );
}

function normalizeEmptyStates() {
  document.querySelectorAll(".empty-state strong").forEach((node) => {
    v4SetText(node, V4_TEXT.emptyTitle);
  });
  document.querySelectorAll(".empty-state p").forEach((node) => {
    v4SetText(node, V4_TEXT.emptyBody);
  });
}

function applyV4() {
  document.body.classList.add("theme-v4");
  applyStaticText();
  reorderTabs();
  ensureActiveTab();
  simplifyTaskStrip();
  simplifyWorkspace();
  normalizeHeroFallbacks();
  relabelMetaCards();
  simplifyRuntimePanel();
  normalizeGuide();
  normalizeEmptyStates();
}

const v4Observer = new MutationObserver(() => {
  window.clearTimeout(v4Observer._timer);
  v4Observer._timer = window.setTimeout(applyV4, 40);
});

window.addEventListener("load", () => {
  applyV4();
  v4Observer.observe(document.body, { childList: true, subtree: true });
});
